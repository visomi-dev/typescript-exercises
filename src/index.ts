// Algunas palabras reservadas:
// number - tipo número
// const - declarar constante (no cambia)
// let - declarar variable
// for - ciclos

// declaramos y asignando el temporizador
let timer: number = 0;

// esperamos hasta el 60
for (let index = 0; index < 60; index++) {
    timer++;
}

// si ya pasaron 60 minutos
if (timer == 60) {
    console.log('Ya está cocinado')
}

